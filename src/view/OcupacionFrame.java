    public void mostrarOcupaciones(List<Ocupacion> ocupaciones) {
        DefaultTableModel model = new DefaultTableModel(
                new Object[]{"ID", "Ocupación"},
                0
        );

        for (Ocupacion o : ocupaciones) {
            model.addRow(new Object[]{
                o.getIdOcupacion(),
                o.getNombre()
            });
        }

        tblOcupacion.setModel(model);
    }
